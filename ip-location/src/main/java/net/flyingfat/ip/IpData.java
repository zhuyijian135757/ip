package net.flyingfat.ip;

public class IpData {

	private String ipStartNum;
	
	private String ipEndNum;
	
	private String ipStartStr;
	
	private String ipEndStr;
	
	private String provinceId;
	
	private String cityId;

	public String getIpStartNum() {
		return ipStartNum;
	}

	public void setIpStartNum(String ipStartNum) {
		this.ipStartNum = ipStartNum;
	}

	public String getIpEndNum() {
		return ipEndNum;
	}

	public void setIpEndNum(String ipEndNum) {
		this.ipEndNum = ipEndNum;
	}

	public String getIpStartStr() {
		return ipStartStr;
	}

	public void setIpStartStr(String ipStartStr) {
		this.ipStartStr = ipStartStr;
	}

	public String getIpEndStr() {
		return ipEndStr;
	}

	public void setIpEndStr(String ipEndStr) {
		this.ipEndStr = ipEndStr;
	}

	public String getProvinceId() {
		return provinceId;
	}

	public void setProvinceId(String provinceId) {
		this.provinceId = provinceId;
	}

	public String getCityId() {
		return cityId;
	}

	public void setCityId(String cityId) {
		this.cityId = cityId;
	}
	
}
